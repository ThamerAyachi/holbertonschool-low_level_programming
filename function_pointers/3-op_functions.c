#include "3-calc.h"

/**
 * op_add - Entry point
 *
 * @a: integer
 *
 * @b: integer
 *
 * Description: 'add.'
 *
 * Return: integer
 */
int op_add(int a, int b) { return (a + b); }
/**
 * op_sub - Entry point
 *
 * @a: integer
 *
 * @b: integer
 *
 * Description: 'sub.'
 *
 * Return: integer
 */
int op_sub(int a, int b) { return (a - b); }
/**
 * op_mul - Entry point
 *
 * @a: integer
 *
 * @b: integer
 *
 * Description: 'mul.'
 *
 * Return: integer
 */
int op_mul(int a, int b) { return (a * b); }
/**
 * op_div - Entry point
 *
 * @a: integer
 *
 * @b: integer
 *
 * Description: 'div.'
 *
 * Return: integer
 */
int op_div(int a, int b) { return (a / b); }
/**
 * op_mod - Entry point
 *
 * @a: integer
 *
 * @b: integer
 *
 * Description: 'mod.'
 *
 * Return: integer
 */
int op_mod(int a, int b) { return (a % b); }
